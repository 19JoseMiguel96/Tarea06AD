    private void btnConfirmarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarRegistroActionPerformed
        
        // Comprobamos que no se deje ningún campo vacío
        if (datosCorrectos()) {
            
            // Comprobar si existe el usuario
            if (db.existeUsuario(txtUsuario.getText())) {
                JOptionPane.showMessageDialog(rootPane, "El usuario '" + txtUsuario.getText() + "' ya existe. Introduce uno nuevo.", FormPrincipal.ATENCION, HEIGHT);
                
            } 
            // Si el usuario no existe, lo guardamos
            else {
                
                Usuario usu = new Usuario(txtNombre.getText(),txtUsuario.getText(), txtContra1.getText(), new ArrayList<String>());
                db.guardarUsuario(usu);
                JOptionPane.showMessageDialog(rootPane, "¡Felicidades! Usuario registrado correctamente.", FormPrincipal.ATENCION, HEIGHT);
                this.dispose();
            }
            
        } else {
            JOptionPane.showMessageDialog(rootPane, errores, FormPrincipal.ATENCION, HEIGHT);
        }
    }//GEN-LAST:event_btnConfirmarRegistroActionPerformed

