    // Método para verificar si se han introducido todos los datos y si se hizo de forma correcta
    private boolean datosCorrectos() {
        
        errores = "";
        
        if (txtNombre.getText().equals("")) {errores = "-El campo del nombre no puede quedar vacío.\n";}
        if (txtUsuario.getText().equals("")) {errores = errores + "-El campo del usuario no puede quedar vacío.\n";}
        if (txtContra1.getText().equals("")) {errores = errores + "-El campo de la contraseña no puede quedar vacía.\n";}
        if (!txtContra2.getText().equals(txtContra1.getText())) errores = errores + "-Las contraseñas no coinciden.";
        
        return errores.equals("");    //Si no hubo errores se devuelve el campo vacío
        
    }


